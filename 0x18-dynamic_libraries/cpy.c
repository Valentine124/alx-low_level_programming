#include "cpy.h"

/**
 * add - add two numbers
 * @a: the first
 * @b: the second
 *
 * Return: result
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 * @a: the first
 * @b: the second
 *
 * Return: result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: the first
 * @b: the second
 *
 * Return: result
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divide two numbers
 * @a: the first
 * @b: the second
 *
 * Return: result
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - divide two numbers and return the remender
 * @a: the first
 * @b: the second
 *
 * Return: result
 */
int mod(int a, int b)
{
	return (a % b);
}
